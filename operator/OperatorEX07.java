package operator;

public class OperatorEX07 {

	public static void main(String[] args) {
		//비교 연산자 ( >(크다) >=(크거나 같다) <(작다) <=(작거나 같다) ==(같다) !=(같지않다)
		//조건문과 반복문을 활용하는데 사용
		int num1=20;
		int num2=10;
		System.out.println(num1 > num2);
		System.out.println(num1 >= num2);
		System.out.println(num1 < num2);
		System.out.println(num1 <= num2);
		System.out.println(num1 == num2);
		System.out.println(num1 != num2);
	}

}
